package com.xa.webui.exception.service;

public class ServiceWorkflowException extends ServiceException {

    public ServiceWorkflowException() {
        super();
    }

    public ServiceWorkflowException(String message, Throwable cause) {
        super(message, cause);
    }

    public ServiceWorkflowException(String message) {
        super(message);
    }

    public ServiceWorkflowException(Throwable cause) {
        super(cause);
    }

}

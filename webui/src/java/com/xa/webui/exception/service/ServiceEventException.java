package com.xa.webui.exception.service;

public class ServiceEventException extends ServiceException {

    public ServiceEventException() {
        super();
    }

    public ServiceEventException(String message, Throwable cause) {
        super(message, cause);
    }

    public ServiceEventException(String message) {
        super(message);
    }

    public ServiceEventException(Throwable cause) {
        super(cause);
    }

}
